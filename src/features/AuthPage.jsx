import React, { useState, useEffect } from 'react';
import { useLocation } from 'wouter';
import { ArrowRight, Mail, Lock, Eye, EyeOff } from 'lucide-react';
import { supabase } from '../services/supabase';
import { useAuth } from '../contexts/AuthContext';
import { C, GLASS, RADIUS } from '../styles/theme';
import ShaderBackground from '../components/ShaderBackground';

const AuthPage = () => {
  const [, navigate] = useLocation();
  const { isLoggedIn, signInWithGoogle, signInWithEmail, signUpWithEmail } = useAuth();

  const [mode, setMode] = useState('login'); // 'login' | 'signup' | 'forgot'
  const [email, setEmail] = useState('');
  const [password, setPassword] = useState('');
  const [showPassword, setShowPassword] = useState(false);
  const [loading, setLoading] = useState(false);
  const [error, setError] = useState(null);
  const [message, setMessage] = useState(null);

  useEffect(() => {
    if (isLoggedIn) navigate('/', { replace: true });
  }, [isLoggedIn, navigate]);

  if (!supabase) {
    return (
      <div style={{ minHeight: '100vh', background: C.bg, color: C.text, display: 'flex', alignItems: 'center', justifyContent: 'center', padding: 20 }}>
        <p style={{ color: C.muted, textAlign: 'center' }}>
          Supabase לא מוגדר. הוסף VITE_SUPABASE_URL ו-VITE_SUPABASE_ANON_KEY לקובץ .env.local
        </p>
      </div>
    );
  }

  const handleSubmit = async (e) => {
    e.preventDefault();
    setError(null);
    setMessage(null);
    setLoading(true);

    try {
      if (mode === 'forgot') {
        const { error: err } = await supabase.auth.resetPasswordForEmail(email, {
          redirectTo: window.location.origin,
        });
        if (err) throw err;
        setMessage('קישור לאיפוס סיסמה נשלח לאימייל שלך');
      } else if (mode === 'login') {
        const { error: err } = await signInWithEmail(email, password);
        if (err) throw err;
      } else {
        const { error: err } = await signUpWithEmail(email, password);
        if (err) throw err;
        setMessage('בדוק את האימייל שלך לאישור ההרשמה');
      }
    } catch (err) {
      setError(err.message);
    } finally {
      setLoading(false);
    }
  };

  const inputStyle = {
    width: '100%', padding: '12px 14px', borderRadius: 12,
    background: 'rgba(255,255,255,0.05)', border: `1px solid ${C.border}`,
    color: C.text, fontSize: 15, outline: 'none', boxSizing: 'border-box',
    direction: 'ltr',
  };

  return (
    <div style={{ minHeight: '100vh', background: C.bg, color: C.text, position: 'relative' }}>
      <ShaderBackground opacity={0.15} />

      <header style={{
        position: 'sticky', top: 0, zIndex: 10, ...GLASS.header,
        padding: '16px 20px', display: 'flex', alignItems: 'center', gap: 12,
      }}>
        <button
          onClick={() => navigate('/')}
          style={{
            width: 38, height: 38, borderRadius: RADIUS.full, background: 'transparent',
            border: 'none', cursor: 'pointer', display: 'flex', alignItems: 'center', justifyContent: 'center',
          }}
        >
          <ArrowRight size={20} color={C.text} />
        </button>
        <h2 style={{ fontSize: 18, fontWeight: 700, margin: 0, color: C.text }}>
          {mode === 'login' ? 'התחברות' : mode === 'signup' ? 'הרשמה' : 'איפוס סיסמה'}
        </h2>
      </header>

      <main style={{ padding: '40px 20px', maxWidth: 400, margin: '0 auto' }}>
        <div style={{ textAlign: 'center', marginBottom: 32 }}>
          <h1 style={{ fontSize: 24, fontWeight: 700, margin: '0 0 8px', color: C.text }}>
            ברוכים הבאים
          </h1>
          <p style={{ fontSize: 14, color: C.muted, margin: 0 }}>
            התחבר כדי לשמור את ההתקדמות שלך בכל מכשיר
          </p>
        </div>

        {/* Google Sign-In */}
        <button
          onClick={() => { setError(null); signInWithGoogle(); }}
          style={{
            width: '100%', padding: '12px 20px', borderRadius: 12,
            background: 'rgba(255,255,255,0.06)', border: `1px solid ${C.border}`,
            color: C.text, fontSize: 15, fontWeight: 600, cursor: 'pointer',
            display: 'flex', alignItems: 'center', justifyContent: 'center', gap: 10,
            marginBottom: 24,
          }}
        >
          <svg width="18" height="18" viewBox="0 0 24 24">
            <path fill="#4285F4" d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92a5.06 5.06 0 0 1-2.2 3.32v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.1z"/>
            <path fill="#34A853" d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"/>
            <path fill="#FBBC05" d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"/>
            <path fill="#EA4335" d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"/>
          </svg>
          המשך עם Google
        </button>

        <div style={{ display: 'flex', alignItems: 'center', gap: 12, marginBottom: 24 }}>
          <div style={{ flex: 1, height: 1, background: C.border }} />
          <span style={{ fontSize: 13, color: C.muted }}>או</span>
          <div style={{ flex: 1, height: 1, background: C.border }} />
        </div>

        {/* Email Form */}
        <form onSubmit={handleSubmit} style={{ display: 'flex', flexDirection: 'column', gap: 14 }}>
          <div>
            <label style={{ display: 'block', fontSize: 13, color: C.muted, marginBottom: 6 }}>אימייל</label>
            <div style={{ position: 'relative' }}>
              <Mail size={16} color={C.dim} style={{ position: 'absolute', left: 14, top: '50%', transform: 'translateY(-50%)' }} />
              <input
                type="email"
                value={email}
                onChange={(e) => setEmail(e.target.value)}
                placeholder="you@example.com"
                required
                style={{ ...inputStyle, paddingLeft: 40 }}
              />
            </div>
          </div>

          {mode !== 'forgot' && (
            <div>
              <label style={{ display: 'block', fontSize: 13, color: C.muted, marginBottom: 6 }}>סיסמה</label>
              <div style={{ position: 'relative' }}>
                <Lock size={16} color={C.dim} style={{ position: 'absolute', left: 14, top: '50%', transform: 'translateY(-50%)' }} />
                <input
                  type={showPassword ? 'text' : 'password'}
                  value={password}
                  onChange={(e) => setPassword(e.target.value)}
                  placeholder={mode === 'signup' ? '6+ תווים' : 'הסיסמה שלך'}
                  required
                  minLength={mode === 'signup' ? 6 : undefined}
                  style={{ ...inputStyle, paddingLeft: 40, paddingRight: 44 }}
                />
                <button
                  type="button"
                  onClick={() => setShowPassword(!showPassword)}
                  style={{
                    position: 'absolute', right: 10, top: '50%', transform: 'translateY(-50%)',
                    background: 'none', border: 'none', color: C.dim, cursor: 'pointer', padding: 4,
                  }}
                >
                  {showPassword ? <EyeOff size={16} /> : <Eye size={16} />}
                </button>
              </div>
            </div>
          )}

          {error && (
            <p style={{ margin: 0, fontSize: 13, color: C.red, textAlign: 'center' }}>{error}</p>
          )}
          {message && (
            <p style={{ margin: 0, fontSize: 13, color: C.green, textAlign: 'center' }}>{message}</p>
          )}

          <button
            type="submit"
            disabled={loading}
            style={{
              width: '100%', padding: '13px 20px', borderRadius: 12, border: 'none',
              background: C.gradient, color: 'white', fontSize: 15, fontWeight: 600,
              cursor: loading ? 'wait' : 'pointer', opacity: loading ? 0.7 : 1,
            }}
          >
            {loading ? '...' : mode === 'login' ? 'התחברות' : mode === 'signup' ? 'הרשמה' : 'שלח קישור לאיפוס'}
          </button>
        </form>

        {/* Toggle links */}
        <div style={{ marginTop: 20, textAlign: 'center', display: 'flex', flexDirection: 'column', gap: 8 }}>
          {mode === 'login' && (
            <>
              <button onClick={() => { setMode('signup'); setError(null); }} style={{ background: 'none', border: 'none', color: C.purple, fontSize: 13, cursor: 'pointer' }}>
                אין לך חשבון? הירשם
              </button>
              <button onClick={() => { setMode('forgot'); setError(null); }} style={{ background: 'none', border: 'none', color: C.muted, fontSize: 12, cursor: 'pointer' }}>
                שכחת סיסמה?
              </button>
            </>
          )}
          {mode === 'signup' && (
            <button onClick={() => { setMode('login'); setError(null); }} style={{ background: 'none', border: 'none', color: C.purple, fontSize: 13, cursor: 'pointer' }}>
              יש לך חשבון? התחבר
            </button>
          )}
          {mode === 'forgot' && (
            <button onClick={() => { setMode('login'); setError(null); }} style={{ background: 'none', border: 'none', color: C.purple, fontSize: 13, cursor: 'pointer' }}>
              חזור להתחברות
            </button>
          )}
        </div>
      </main>
    </div>
  );
};

export default AuthPage;
